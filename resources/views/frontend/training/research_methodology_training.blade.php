                                    Methodology of the Study: To supplement the information gathered from literature,
                                    personal interviews were conducted with representative of a number of professional
                                    accounting firms in Dhaka city. For that purpose 25 Chartered Accountant Firms were
                                    selected at random out of 150 (there are 182 C.A. firms in Bangladesh according to ICAB
                                    Report, July 1, 1995) located only in Dhaka city due to financial and other constraints.

                                    Results of the Study: Integrating statistical sampling into the audit planning process
                                    aids the auditor in his effort to control the sampling risk associated with forming an
                                    opinion on the financial statements. Integrating statistical sampling into the auditing
                                    requires careful planning, proper execution and appropriate evaluation of the sample
                                    results.
                                    The study explains the basic sampling concepts such as reliability levels, precision,
                                    materiality, risk and related aspects which really characterize the audit sampling
                                    situation generally experienced by an auditor and also explains how these concepts fit
                                    into the auditing theory.
                                    Different audit firms adopt different attitudes towards statistical sampling. Some firms
                                    use sampling widely, others use in a very restricted circumstances or even not at all.
                                    Many firms might have made changes to their approaches as a result of experience of
                                    themselves and others, in the country and abroad.

                                    From the study, it is evident that there is a little space for discovery sampling I the
                                    outside auditorâ€™s audit program. This leaves the monetary unit sampling (MUS) as the
                                    statistical sampling technique which has proved to be the most useful to the outside
                                    auditors. Eighty percent of the sample respondents even in Bangladesh considered this
                                    method most important and useful for better audit performance. However, variable
                                    estimation is of only limited use. This leaves the attribute estimation technique as the
                                    statistical sampling technique which has the greatest utility to the external auditors.
                                    Statistical sampling techniques have been widely used to test the internal control
                                    procedures. The study also reveals that although most of the auditors are aware of the
                                    audit sampling techniques but a very few of them have working knowledge to apply them in
                                    proper perspective.
                                    Recommendations for integrating statistical sampling into audit procedures: In order to
                                    accelerate audit sampling practices, the following recommendations may be considered
                                    useful -- commitment to apply sampling, top management positive decision, requirement of
                                    statistical knowledge, homogeneity of population, design of audit program, analysis of
                                    the population, motivation of clients, programmed approach, and use of computer.
