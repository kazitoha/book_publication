                                    seven hats and in three hats, there are no such toll collectors. In these hats, tolls
                                    are collected by
                                    the actual izaradars themselves. The Local Government i.e. the Thana Parishad leases out
                                    the
                                    hats to the izaradar on auction basis for one year (Bangali year). Physical development
                                    of the hat
                                    is a responsibility of the local government. Some larger hats have their own Committees.

                                    The study makes a number of recommendations: (i) Owning of hat areas and responsibility
                                    of hat
                                    management should go under one authority, so that one single authority can control the
                                    affairs of
                                    the village hat. (ii) The appropriate authority may be requested to fix the rate of toll
                                    for each hat
                                    payable by each buyer/seller for each commodity. This may help the toll collection more
                                    rational
                                    and impartial. And (iii) display of toll chart should be made obligatory on the part of
                                    the
                                    izaradars on each hat day. The collection of excessive tolls by the izaradars should be
                                    banned
                                    imposing penalties.
